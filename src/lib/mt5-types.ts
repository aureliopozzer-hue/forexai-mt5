/**
 * MT5 Auto-Trading Type Definitions
 *
 * These types define the data structures for the MT5 auto-trading bridge.
 * The Python bot on VPS and the Vercel API communicate via Supabase (shared state).
 *
 * Tables in Supabase:
 * - mt5_signals    — AI-generated trade signals awaiting auto-execution
 * - mt5_positions  — Open/closed positions synced from MT5
 * - mt5_bot_status — Bot connection status (single row, updated by bot)
 * - mt5_config     — Auto-trading configuration (single row, updated by user)
 * - mt5_accounts   — Multi-client broker accounts with risk management
 * - mt5_daily_logs — Daily P&L tracking history
 * - mt5_trade_log  — Detailed trade log
 */

// ======================== MT5 SIGNAL ========================

export type MT5SignalStatus = 'pending' | 'executed' | 'skipped' | 'expired' | 'failed';
export type MT5SignalDirection = 'BUY' | 'SELL';

/** MT5 Signal — generated by AI analysis, waiting for auto-execution */
export interface MT5Signal {
  id: string;
  symbol: string;
  direction: MT5SignalDirection;
  entry_price: number;
  stop_loss: number;
  take_profit: number;
  confidence: number;
  strategy: string;
  lot_size: number;
  status: MT5SignalStatus;
  error_message?: string;
  created_at: string;
  executed_at?: string;
}

/** Payload for creating a new signal */
export interface MT5SignalCreatePayload {
  symbol: string;
  direction: MT5SignalDirection;
  entry_price: number;
  stop_loss: number;
  take_profit: number;
  confidence: number;
  strategy: string;
  lot_size: number;
}

