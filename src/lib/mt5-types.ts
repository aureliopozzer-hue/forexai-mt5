// ======================== MT5 CONFIG ========================

/** Lot sizing mode */
export type MT5LotType = 'fixed' | 'percentage';

/** MT5 account type */
export type MT5AccountType = 'demo' | 'live';

/** MT5 Config — auto-trading configuration (single row, updated by user) */
export interface MT5Config {
  id: string;
  auto_trading_enabled: boolean;
  max_lot_size: number;
  risk_per_trade_pct: number;
  allowed_symbols: string[];  // stored as JSON string in Supabase
  max_open_positions: number;
  min_confidence: number;
  strategy_filter: string[];  // stored as JSON string in Supabase
  stop_loss_default_pips: number;
  take_profit_default_pips: number;
  trading_hours_start: string;  // "09:00"
  trading_hours_end: string;    // "17:00"
  updated_at: string;

  // Broker connection (client enters credentials on the website)
  mt5_login: number;           // MT5 account number
  mt5_password: string;        // MT5 password (stored in Supabase, read by bot)
  mt5_server: string;          // Broker server (e.g., "MetaQuotes-Demo")
  mt5_account_type: MT5AccountType;  // 'demo' or 'live'

  // Risk management
  profit_target: number;       // Daily profit target in account currency (0 = disabled)
  loss_limit: number;          // Daily loss limit in account currency (0 = disabled)
  lot_type: MT5LotType;        // 'fixed' = use fixed_lot, 'percentage' = calculate from balance
  lot_percentage: number;      // Risk % per trade when lot_type='percentage'
  fixed_lot: number;           // Fixed lot size when lot_type='fixed'

  // Daily P&L tracking (updated by bot)
  daily_pnl: number;           // Accumulated daily P&L
  daily_pnl_date: string | null;  // Date of current daily P&L tracking
}

/** Payload for updating config (partial, all fields optional) */
export interface MT5ConfigUpdatePayload {
  auto_trading_enabled?: boolean;
  max_lot_size?: number;
  risk_per_trade_pct?: number;
  allowed_symbols?: string[];
  max_open_positions?: number;
  min_confidence?: number;
  strategy_filter?: string[];
  stop_loss_default_pips?: number;
  take_profit_default_pips?: number;
  trading_hours_start?: string;
  trading_hours_end?: string;

  // Broker connection
  mt5_login?: number;
  mt5_password?: string;
  mt5_server?: string;
  mt5_account_type?: MT5AccountType;

  // Risk management
  profit_target?: number;
  loss_limit?: number;
  lot_type?: MT5LotType;
  lot_percentage?: number;
  fixed_lot?: number;

  // Daily P&L (updated by bot)
  daily_pnl?: number;
  daily_pnl_date?: string | null;
}

